// -------------------------------
// Arrays in JavaScript
// -------------------------------

// Array declaration (collection of elements)
let arr = [1, 2, 3, 5, 9, 7, 8];
console.log(arr);  // [1, 2, 3, 5, 9, 7, 8]

// -------------------------------
// Iteration using for-of loop
// -------------------------------
// for-of gives direct access to each element in the array
for (let value of arr) {
    console.log(value);
}
