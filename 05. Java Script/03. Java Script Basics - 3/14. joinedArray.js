// -------------------------------
// Joining Array Elements
// -------------------------------

// The join() method joins all elements of an array into a single string
// You can specify a separator (default is comma)

let first = [1, 2, 3, 4];

// Joining array elements with underscore "_"
let joined = first.join('_');
console.log(joined);   // Output: "1_2_3_4"
