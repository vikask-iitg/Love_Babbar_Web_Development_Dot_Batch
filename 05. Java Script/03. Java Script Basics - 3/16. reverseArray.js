// -------------------------------
// Reversing an Array
// -------------------------------

// The reverse() method reverses the elements of an array in place.
// It changes the original array as well.

let first = [1, 3, 5, 2];
console.log("Original Array:", first);

// Reverse the array
let reverse = first.reverse();
console.log("Reversed Array:", reverse);

// Note: reverse() modifies the original array too
console.log("Modified Original Array:", first);
