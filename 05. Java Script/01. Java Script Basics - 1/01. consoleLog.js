// 1. Simple string
console.log("Vikas");

// 2. Using single quotes
console.log('Kumar');

// 3. Using template literals
console.log(`Khurendra`)

// 4. Using a variable
let myName = "Manoj";
console.log(myName);

// 5. Concatenation
console.log("My name is " + "Bajpai");

// 6. Template literals with expression
let firstName = "Manoj";
console.log(`My name is ${firstName}`);

// 7. Multiple arguments
console.log("Name:", "Rajpal");

// 8. Array style
console.log(["Yadav"]);

// 9. Object style
console.log({
    myName: "Pankaj Tripathi"
})

// 10. Function call
function printName(n) {
    console.log(n);
}
printName("Bacchan");