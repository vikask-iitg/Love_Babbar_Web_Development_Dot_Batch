// #1 - Single If-Else
let number = 10;

if (number > 10) {
    console.log("Number is greater than 10");
} else {
    console.log("Number is lesser or equal to 10");
}
// Output: Number is lesser or equal to 10


// #2 - Multiple If-Else (else-if ladder)
if (number < 10) {
    console.log("Number is lesser than 10");
} else if (number > 10) {
    console.log("Number is greater than 10");
} else {
    console.log("Number is equal to 10");
}
// Output: Number is equal to 10
