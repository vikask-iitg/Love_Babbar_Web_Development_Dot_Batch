// ------------------------------
// Window Object in JavaScript
// ------------------------------
// In browsers, the global object is `window`.
// All global variables, functions, and browser-provided APIs
// (like console, alert, setTimeout, etc.) are part of `window`.

window.console.log("Hello");   // Access console via window object
console.log("Hello");          // Same as above (window is implied)

// Alert boxes
alert("This is an alert!");          // Shorthand
window.alert("This is also an alert!"); // Explicit reference to window
